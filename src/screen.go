func DrawBitmap(bmp []uint16) {
	rl.ClearBackground(CLR_BG)
	var x, y int32
	for y = 0; y < 16; y++ {
		for x = 0; x < 16; x++ {
			if (bmp[y]>>(SCREEN_SIZE-1-x))%2 == 1 {
				rl.DrawRectangle(
					x*PIXEL_SIZE, y*PIXEL_SIZE,
					PIXEL_SIZE, PIXEL_SIZE,
					CLR_FG,
				)
			}
		}
	}
}
